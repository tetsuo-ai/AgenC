    async testVerifierCPI(): Promise<TestResult> {
        this.log('Testing CPI to verifier (proof verification)...');
        
        // Extract just the public inputs from the witness
        // The witness file has header (12 bytes) then 35 x 32-byte public inputs
        // Public inputs are: task_id (1), agent_pubkey (32 bytes as fields), constraint_hash (1), output_commitment (1)
        
        // From the witness, the actual public inputs start after the header
        // constraint_hash: 0x224785a48a72c75e2cbb698143e71d5d41bd89a2b9a7185871e39a54ce5785b1
        // output_commitment: 0x2a4c1b6d1dbda0140b9f9440e8be130b2547074d1db76f96f8c815343bb2239a
        
        const constraintHash = Buffer.from([
            0x22, 0x47, 0x85, 0xa4, 0x8a, 0x72, 0xc7, 0x5e, 0x2c, 0xbb, 0x69, 0x81,
            0x43, 0xe7, 0x1d, 0x5d, 0x41, 0xbd, 0x89, 0xa2, 0xb9, 0xa7, 0x18, 0x58,
            0x71, 0xe3, 0x9a, 0x54, 0xce, 0x57, 0x85, 0xb1
        ]);
        
        const outputCommitment = Buffer.from([
            0x2a, 0x4c, 0x1b, 0x6d, 0x1d, 0xbd, 0xa0, 0x14, 0x0b, 0x9f, 0x94, 0x40,
            0xe8, 0xbe, 0x13, 0x0b, 0x25, 0x47, 0x07, 0x4d, 0x1d, 0xb7, 0x6f, 0x96,
            0xf8, 0xc8, 0x15, 0x34, 0x3b, 0xb2, 0x23, 0x9a
        ]);

        // Sunspot verifier expects: proof bytes only, public inputs passed as accounts or separate
        // Try just the proof first
        const instructionData = REAL_PROOF;

        try {
            const ix = new TransactionInstruction({
                programId: VERIFIER_PROGRAM_ID,
                keys: [
                    { pubkey: this.worker.publicKey, isSigner: true, isWritable: false },
                ],
                data: instructionData,
            });

            const tx = new Transaction().add(ix);
            
            // Simulate first
            const simulation = await this.connection.simulateTransaction(tx, [this.worker]);
            
            this.log(`Simulation logs: ${JSON.stringify(simulation.value.logs)}`);
            
            if (simulation.value.err) {
                return {
                    step: 'Verifier CPI (Simulation)',
                    success: false,
                    error: `Simulation failed: ${JSON.stringify(simulation.value.err)}`,
                    logs: simulation.value.logs,
                };
            }

            const sig = await sendAndConfirmTransaction(this.connection, tx, [this.worker]);
            
            return {
                step: 'Verifier CPI',
                success: true,
                txSignature: sig,
            };
        } catch (e: any) {
            return {
                step: 'Verifier CPI',
                success: false,
                error: e.message,
            };
        }
    }

