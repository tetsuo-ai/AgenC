interface TestResult {
    step: string;
    success: boolean;
    txSignature?: string;
    error?: string;
    logs?: string[] | null;
}

async function loadKeypair(filepath: string): Promise<Keypair> {
    const secretKey = JSON.parse(fs.readFileSync(filepath, 'utf-8'));
    return Keypair.fromSecretKey(new Uint8Array(secretKey));
}

async function requestAirdrop(connection: Connection, pubkey: PublicKey, amount: number): Promise<string> {
    const sig = await connection.requestAirdrop(pubkey, amount * LAMPORTS_PER_SOL);
    await connection.confirmTransaction(sig);
    return sig;
}

async function getBalance(connection: Connection, pubkey: PublicKey): Promise<number> {
    const balance = await connection.getBalance(pubkey);
    return balance / LAMPORTS_PER_SOL;
}

